function login(){
	if($(".user_code").val()!=''&&$(".user_password").val()!=''){
		$.ajax({
			url:"/websocket/log/login",
			data:{
				user_code:$(".user_code").val(),
				user_password:$(".user_password").val(),
				login_type:2
			},
			dataType:"json",
			success:function(e){
				if(e.flag==1){
					setCookie("user_token",e.data.token);
					setCookie("login_type",e.data.type);
					setCookie("userJson",JSON.stringify(e.data.user));
					window.location.href="webSocket.html"
				}else{
					alert(e.msg)
				}
			},
			error:function(e){
				console.log(e);
			}
		})
	}else{
		alert("有空项")
	}
}